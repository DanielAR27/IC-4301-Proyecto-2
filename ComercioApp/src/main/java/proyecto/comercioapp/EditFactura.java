    private void cargarDatosFactura(){
        List <Object> facturaInfo = DBMediator.getFacturaPorID(facturaID);
        facturaIDLabel.setText("Factura ID: " + facturaID);
        facturaDateChooser.setDate((Date) facturaInfo.get(0));
        totalLabel.setText("Total: £ "+ Float.toString((Float) facturaInfo.get(1)));
        costoTextField.setText(Float.toString((Float) facturaInfo.get(2)));
        estadoComboBox.setSelectedItem(facturaInfo.get(3));
    }
    

