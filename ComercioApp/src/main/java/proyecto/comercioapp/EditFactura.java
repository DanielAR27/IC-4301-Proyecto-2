    private void acceptButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acceptButtonActionPerformed
        try{
            String fechaFactura = formatoFecha.format(facturaDateChooser.getDate());
            Float costoEnvio = Float.valueOf(costoTextField.getText());
            String estado = (String) estadoComboBox.getSelectedItem();
            
            int res = DBMediator.updateFactura(facturaID, fechaFactura, costoEnvio, estado);
            switch(res){
                case 0->{
                     JOptionPane.showMessageDialog(this, "Se ha actualizado exitosamente la factura.", "Notificación", JOptionPane.INFORMATION_MESSAGE);
                     parent.resetearPagina();
                     parent.actualizarPanelesFacturas(0); // Llama al método para actualizar la lista
                    this.dispose();                   
                }case -1 ->{
                    JOptionPane.showMessageDialog(this, "No se ha encontrado la factura por su ID.", "Advertencia", JOptionPane.WARNING_MESSAGE);
                }default ->{
                    JOptionPane.showMessageDialog(this, "Verifique llenar todos los espacios correctamente.", "Advertencia", JOptionPane.WARNING_MESSAGE);                    
                }
            }
       }catch(NumberFormatException nfe){
              JOptionPane.showMessageDialog(this, "Verifique que el costo ingresado sea un número cualquiera.", "Advertencia", JOptionPane.WARNING_MESSAGE);           
       }catch (NullPointerException npe){
              JOptionPane.showMessageDialog(this, "Verifique haber ingresado una fecha antes de continuar.", "Advertencia", JOptionPane.WARNING_MESSAGE);
              return;              
       }catch (Exception e){
                e.printStackTrace();
               JOptionPane.showMessageDialog(this, "Ha ocurrido un error, intente de nuevo más tarde.", "Advertencia", JOptionPane.ERROR_MESSAGE);
              return;                
       }   
    }//GEN-LAST:event_acceptButtonActionPerformed

